let currentLang = 'ko';

export function detectLang() {
  const saved = localStorage.getItem('quiz_lang');
  if (saved) return saved;
  const br = navigator.language?.split('-')[0] ?? 'en';
  return SUPPORTED_LANGS.find(l => l.code === br) ? br : 'en';
}

export function getLang() { return currentLang; }

export function setLang(code) {
  currentLang = code;
  localStorage.setItem('quiz_lang', code);
  document.documentElement.lang = code;
  document.documentElement.dir = code === 'ar' ? 'rtl' : 'ltr';
}

export function initLang() {
  setLang(detectLang());
}

export function t(key) {
  const dict = T[currentLang] || T['en'];
  return dict?.[key] ?? T['ko']?.[key] ?? key;
}

